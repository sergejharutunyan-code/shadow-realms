// Generates src/lib/hero-palettes.ts by sampling each hero portrait.
//
// For every champion we extract a small colour identity from the actual
// art — primary (garment/armour, sampled from the torso band), secondary
// (upper body / hair band), accent (the most saturated frequent colour)
// and glow (ambient backdrop) — so the 3D figurines can be tinted to
// match their hero. Run: node scripts/generate-hero-palettes.mjs
import sharp from 'sharp';
import { readdirSync, writeFileSync, readFileSync } from 'node:fs';
import path from 'node:path';

const ROOT = path.resolve(import.meta.dirname, '..');
const HEROES_DIR = path.join(ROOT, 'public', 'heroes');
const OUT = path.join(ROOT, 'src', 'lib', 'hero-palettes.ts');

// templateId -> portrait path mapping lives in hero-images.ts; parse it.
const mapSrc = readFileSync(path.join(ROOT, 'src', 'lib', 'hero-images.ts'), 'utf8');
const entries = [...mapSrc.matchAll(/'([a-z]+_\d+)':\s*'\/heroes\/([^']+)'/g)].map(m => [m[1], m[2]]);

const W = 48, H = 64;

const clamp = (v, lo, hi) => Math.max(lo, Math.min(hi, v));
const hex = (r, g, b) => '#' + [r, g, b].map(v => clamp(Math.round(v), 0, 255).toString(16).padStart(2, '0')).join('');

function satOf(r, g, b) {
  const mx = Math.max(r, g, b), mn = Math.min(r, g, b);
  return mx === 0 ? 0 : (mx - mn) / mx;
}

// Average colour of a horizontal band, brightened to a usable material tone.
function bandAvg(px, y0, y1, minLum = 46) {
  let r = 0, g = 0, b = 0, n = 0;
  for (let y = Math.floor(H * y0); y < H * y1; y++) {
    for (let x = Math.floor(W * 0.2); x < W * 0.8; x++) {
      const i = (y * W + x) * 3;
      r += px[i]; g += px[i + 1]; b += px[i + 2]; n++;
    }
  }
  r /= n; g /= n; b /= n;
  const lum = 0.299 * r + 0.587 * g + 0.114 * b;
  if (lum < minLum) { const k = (minLum + 14) / (lum + 1); r *= k; g *= k; b *= k; }
  return hex(r, g, b);
}

// Most frequent saturated colour bucket — the hero's signature accent.
function accentOf(px) {
  const buckets = new Map();
  for (let i = 0; i < px.length; i += 3) {
    const r = px[i], g = px[i + 1], b = px[i + 2];
    const lum = 0.299 * r + 0.587 * g + 0.114 * b;
    if (satOf(r, g, b) < 0.45 || lum < 40 || lum > 235) continue;
    const key = `${r >> 5},${g >> 5},${b >> 5}`;
    const cur = buckets.get(key) ?? { r: 0, g: 0, b: 0, n: 0 };
    cur.r += r; cur.g += g; cur.b += b; cur.n++;
    buckets.set(key, cur);
  }
  let best = null;
  for (const v of buckets.values()) if (!best || v.n > best.n) best = v;
  if (!best) return '#c9a227';
  // Boost toward full saturation for a vivid trim colour.
  let { r, g, b, n } = best; r /= n; g /= n; b /= n;
  const mx = Math.max(r, g, b);
  const k = 210 / (mx || 1);
  return hex(r * k, g * k, b * k);
}

const out = {};
for (const [id, file] of entries) {
  const { data } = await sharp(path.join(HEROES_DIR, file))
    .resize(W, H, { fit: 'cover' })
    .removeAlpha()
    .raw()
    .toBuffer({ resolveWithObject: true });
  out[id] = {
    primary: bandAvg(data, 0.52, 0.86),   // torso / garment
    secondary: bandAvg(data, 0.30, 0.52), // chest / hair line
    accent: accentOf(data),               // signature saturated colour
    glow: bandAvg(data, 0.02, 0.2, 34),   // ambient backdrop mood
  };
  console.log(id.padEnd(14), JSON.stringify(out[id]));
}

const ts = `// AUTO-GENERATED by scripts/generate-hero-palettes.mjs — do not edit by hand.
// Per-champion colour identities sampled from the portrait art in public/heroes.
export interface HeroPalette { primary: string; secondary: string; accent: string; glow: string }

export const HERO_PALETTES: Record<string, HeroPalette> = ${JSON.stringify(out, null, 2)};

export function getHeroPalette(templateId: string): HeroPalette {
  return HERO_PALETTES[templateId] ?? { primary: '#6b7280', secondary: '#4b5563', accent: '#c9a227', glow: '#312e81' };
}
`;
writeFileSync(OUT, ts);
console.log('\nwrote', OUT, `(${entries.length} heroes)`);
